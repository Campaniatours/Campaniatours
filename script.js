// ==================== SCROLL REVEAL ====================
const revealElements = document.querySelectorAll('.reveal');

const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('active');
        }
    });
}, {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
});

revealElements.forEach(el => revealObserver.observe(el));

// ==================== BOOKING MODAL ====================
let currentTour = '';
let currentPrice = 0;

function openBooking(tourName, price) {
    currentTour = tourName;
    currentPrice = price;
    
    document.getElementById('modalTourName').textContent = tourName;
    document.getElementById('modalPrice').textContent = '€' + price;
    updateTotal();
    
    const modal = document.getElementById('bookingModal');
    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
}

function closeBooking() {
    const modal = document.getElementById('bookingModal');
    modal.classList.remove('active');
    document.body.style.overflow = '';
    
    // Reset form
    document.getElementById('bookingForm').reset();
}

function updateTotal() {
    const people = parseInt(document.getElementById('bookPeople').value) || 1;
    const total = currentPrice * people;
    
    const peopleText = people === 1 
        ? (currentLang === 'it' ? '1 persona' : currentLang === 'en' ? '1 person' : '1 persona')
        : people + (currentLang === 'it' ? ' persone' : currentLang === 'en' ? ' people' : ' personas');
    
    document.getElementById('modalPeople').textContent = peopleText;
    document.getElementById('modalTotal').textContent = '€' + total;
}

function selectPayment(element) {
    document.querySelectorAll('.payment-method').forEach(el => {
        el.classList.remove('selected');
    });
    element.classList.add('selected');
}

